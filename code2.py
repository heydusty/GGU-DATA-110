# Code 2: Variables
# Variables are used to store data that can be referenced and manipulated during program execution.
# Unlike Java, Python variables do not require explicit declaration of type.
# The type is inferred based on the value assigned.

Var = 3
var = 45

print(Var)   # 3
print(var)   # 45

# Python is case-sensitive: Var and var are different variables
print(Var == var)   # False

x = 34
y = 45
print(x > y)    # False
print(x != y)   # True

